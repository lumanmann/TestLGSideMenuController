import UIKit

extension UIColor {
    
    @nonobjc class var darkPink: UIColor {
        return UIColor(red: 212.0 / 255.0, green: 57.0 / 255.0, blue: 135.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var purply: UIColor {
        return UIColor(red: 160.0 / 255.0, green: 67.0 / 255.0, blue: 182.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var redViolet: UIColor {
        return UIColor(red: 165.0 / 255.0, green: 4.0 / 255.0, blue: 106.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var brownGrey: UIColor {
        return UIColor(red: 126.0 / 255.0, green: 122.0 / 255.0, blue: 122.0 / 255.0, alpha: 1.0)
    }
    
}
